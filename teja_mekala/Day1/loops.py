# for loop
for i in range(20):
    print(i)
# while loop
number = 1
while number <= 3:
    print(number)
    number = number + 1
# if-else
i = 25
if i <= 25:
    print("it is smaller")
else:
    print("it is larger")
# swapping numbers
a = 12
b = 15
# before swapping
print(a, b)
a, b = b, a
# after swapping
print(a, b)
