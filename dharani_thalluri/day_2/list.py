# append()
a = ["apple", "banana", "cherry"]
b = ["Ford", "BMW", "Volvo"]
a.append(b)
print(a)
# extend()
apps = ["instagram", "snapchat", "app store"]
names = ["Dharni", "Bharani", "Varma"]
apps.extend(names)
print(apps)
# insert()
companies = ["IBM", "Eknal", "Infosys"]
companies.insert(1, "Google")
print(companies)
# remove()
TVs = ["appleTV", "AndroidTV", "One PlusTV"]
TVs.remove("AndroidTV")
print(TVs)
# pop()
foods = ["Biryani", "Fruits", "Currys"]
foods.pop(1)
print(foods)
# clear()
banks = ["SBI", "Union", "APGVB"]
banks.clear()
print(banks)
# index()
fruits = ["apple", "banana", "cherry"]
x = fruits.index("cherry")
print(x)
# count()
things = ["bag", "ball", "cherry", "toy", "ball", "bat", "ball"]
x = things.count("ball")
print(x)
# sort()
cars = ["Ford", "BMW", "Volvo"]
cars.sort()
print(cars)
# reverse()
fruits = ["apple", "banana", "cherry"]
fruits.reverse()
print(fruits)
# copy()
colors = ["yellow", "blue", "cream", "red", "orange"]
x = colors.copy()
print(x)
